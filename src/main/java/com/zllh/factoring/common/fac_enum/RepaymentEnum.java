package com.zllh.factoring.common.fac_enum;

/**
 * @ClassName: RepaymentEnum
 * @Description: 还款状态枚举
 * @author JW
 * @date 2016年1月7日 上午11:25:48
 */
public enum RepaymentEnum {

	WAITING_REFUND(1, "待还款"), REFUNDED(2, "已还款");

	private String text;
	private Integer value;

	private RepaymentEnum(Integer value, String text) {
		this.text = text;
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public Integer getValue() {
		return value;
	}
}
