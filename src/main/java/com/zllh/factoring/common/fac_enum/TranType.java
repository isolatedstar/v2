
package com.zllh.factoring.common.fac_enum;

/**
 * @ClassName: TranType
 * @Description: 转账类型枚举
 * @author JW
 * @date 2016年3月16日 上午9:18:39
 */
public enum TranType {
	
	BF("BF", "转账"), BG("BG", "解冻"), BH("BH", "解冻支付"), BR("BR", "冻结"), BS("BS", "支付冻结");

	private String text;
	private String value;

	private TranType(String value, String text) {
		this.text = text;
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public String getValue() {
		return value;
	}
}
