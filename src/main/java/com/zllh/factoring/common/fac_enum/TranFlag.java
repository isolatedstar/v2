
package com.zllh.factoring.common.fac_enum;

/**
 * @ClassName: TranFlag
 * @Description: 转账时效标识枚举
 * @author JW
 * @date 2016年3月16日 上午9:18:18
 */
public enum TranFlag {
	
	YIBU(0, "异步交易"), TONGBU(1, "同步交易");

	private String text;
	private Integer value;

	private TranFlag(Integer value, String text) {
		this.text = text;
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public Integer getValue() {
		return value;
	}
}
