
package com.zllh.factoring.common.model.message.guaranteerefund;

import java.math.BigDecimal;

public class GuaranteeRefund {

	private String guaranteereId;
	
	private BigDecimal refundAmount;

	public String getGuaranteereId() {
		return guaranteereId;
	}

	public void setGuaranteereId(String guaranteereId) {
		this.guaranteereId = guaranteereId;
	}

	public BigDecimal getRefundAmount() {
		return refundAmount;
	}

	public void setRefundAmount(BigDecimal refundAmount) {
		this.refundAmount = refundAmount;
	}

	public GuaranteeRefund(String guaranteereId, BigDecimal refundAmount) {
		super();
		this.guaranteereId = guaranteereId;
		this.refundAmount = refundAmount;
	}

	public GuaranteeRefund() {
		super();
	}
}
