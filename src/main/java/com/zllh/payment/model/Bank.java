package com.zllh.payment.model;

import java.util.Date;
import java.util.List;

public class Bank {

	// 绑定节点的标识值
	private String id;

	// 显示的节点文本
	private String text;

	// 显示的节点文本
	private List<Bank> children;

	// 显示节点的图标
	private String iconCls;

	// 节点是否被选中
	private String checked;

	// 节点状态，'open' 或 'closed'。
	private String state;

	// 绑定该节点的自定义属性。
	private String attributes;

	private String parentBankId;
	
	private String addressId;

	private Date createTime;

	private int status;

	private int lockFlag;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public List<Bank> getChildren() {
		return children;
	}

	public void setChildren(List<Bank> children) {
		this.children = children;
	}

	public String getIconCls() {
		return iconCls;
	}

	public void setIconCls(String iconCls) {
		this.iconCls = iconCls;
	}

	public String getChecked() {
		return checked;
	}

	public void setChecked(String checked) {
		this.checked = checked;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getAttributes() {
		return attributes;
	}

	public void setAttributes(String attributes) {
		this.attributes = attributes;
	}

	public String getParentBankId() {
		return parentBankId;
	}

	public void setParentBankId(String parentBankId) {
		this.parentBankId = parentBankId;
	}
	
	public String getAddressId() {
		return addressId;
	}

	public void setAddressId(String addressId) {
		this.addressId = addressId;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getLockFlag() {
		return lockFlag;
	}

	public void setLockFlag(int lockFlag) {
		this.lockFlag = lockFlag;
	}

	@Override
	public String toString() {
		return "Bank [id=" + id + ", text=" + text + ", children=" + children + ", iconCls=" + iconCls + ", checked=" + checked + ", state=" + state + ", attributes=" + attributes + ", parentBankId=" + parentBankId + ", addressId=" + addressId + ", createTime=" + createTime + ", status=" + status + ", lockFlag=" + lockFlag + "]";
	}

	

}