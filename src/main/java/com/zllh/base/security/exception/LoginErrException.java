
package com.zllh.base.security.exception;

import org.springframework.security.core.AuthenticationException;

/**
 * @ClassName: LoginErrException
 * @Description: 登录过期异常
 * @author JW
 * @date 2016年4月29日 上午10:12:41
 */
public class LoginErrException extends AuthenticationException{

	private static final long serialVersionUID = 1L;

	public LoginErrException(String msg) {
		super(msg);
	}

	public LoginErrException(String msg, Throwable t) {
		super(msg, t);
	}
}
